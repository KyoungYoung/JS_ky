// 산술 연산자 (Arithmetic operators)
/**
 * + 더하기
 * - 빼기
 * / 나누기
 * % 나머지
 * **지수값
 * * 곱하기
 */

let a = 1;
a = -a;
console.log(a);

let boolean = true;
console.log(boolean);
console.log(!boolean);

console.clear(); // + 숫자가 아닌 타입들을 숫자로 변환하면 어떤값이 나오는지 확인
console.log(+false);
console.log(+null);
console.log(+undefined);
console.log(+true);

console.log(!!1); // 부정 연산자

